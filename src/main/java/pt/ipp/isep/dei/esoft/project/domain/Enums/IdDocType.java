package pt.ipp.isep.dei.esoft.project.domain.Enums;

/**
 * The IdDocType enum represents the types of identification documents.
 * It has four types: CC (Citizen Card), BI (Identity Card), PASSPORT, and OTHER.
 */
public enum IdDocType {
        /**
         * Represents a Citizen Card.
         */
        CC,
        /**
         * Represents an Identity Card.
         */
        BI,
        /**
         * Represents a Passport.
         */
        PASSPORT,
        /**
         * Represents any other type of identification document.
         */
        OTHER;
}
